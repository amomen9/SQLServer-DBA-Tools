DECLARE @n INT = 0, @v nvarchar(100);
WHILE @n < 10000
BEGIN
    BEGIN TRY 
        SELECT @v = CONCAT(@n, N'       ', CONVERT(NVARCHAR(50), GETDATE(), @n));
        RAISERROR(@v, 0, 1);
        SET @n += 1;
    END TRY
    BEGIN CATCH
        SET @n += 1;
    END CATCH;
END; 